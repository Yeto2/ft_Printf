#include "ft_printf.h"

void	ft_putnbr_u(unsigned int n, int *count)
{
	if (n > 9)
	{
		ft_putnbr(n / 10, count);
		ft_putnbr(n % 10, count);
	}
	else if (n >= 0 && n <= 9)
	{
		ft_putchar(n + '0', count);
	}
}
